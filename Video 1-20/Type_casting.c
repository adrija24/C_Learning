#include <stdio.h>
int main()
{
    // Type casting....
    printf("We are going to start type casting\n");
    int a = 4;
    float b = 54.4;
    printf("The number is %d\n", (int) b);

    //Type conversion....
    float p = 54/4;
    printf("The result is %f\n", p);

    float r = (float)55/4;
    printf("The result is %f\n", r);
    return 0;
}
 