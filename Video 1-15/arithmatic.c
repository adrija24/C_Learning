#include <stdio.h>

int main()
{
    /* code */
   int a, b;
   a= 34;
   b= 6;

   printf("a + b =%d\n", a+b);
   printf("a - b =%d\n", a-b);
   printf("a * b =%d\n", a*b);
   printf("a / b =%d\n", a/b);


   int p;
   float r;
   p = 34;
   r = 6.34;

   printf("p + r = %f\n", p+r);
   printf("p - r = %f\n", p-r);
   printf("p * r = %f\n", p*r);
   printf("p / r = %f\n", p/r);

    return 0;
}
